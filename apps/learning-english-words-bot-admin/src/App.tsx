import React, { useEffect, useState } from "react";
import { Admin, DataProvider, Resource } from "react-admin";
import buildGraphQLProvider from "./data-provider/graphqlDataProvider";
import { theme } from "./theme/theme";
import Login from "./Login";
import "./App.scss";
import Dashboard from "./pages/Dashboard";
import { WordList } from "./word/WordList";
import { WordCreate } from "./word/WordCreate";
import { WordEdit } from "./word/WordEdit";
import { WordShow } from "./word/WordShow";
import { UserWordList } from "./userWord/UserWordList";
import { UserWordCreate } from "./userWord/UserWordCreate";
import { UserWordEdit } from "./userWord/UserWordEdit";
import { UserWordShow } from "./userWord/UserWordShow";
import { QuizList } from "./quiz/QuizList";
import { QuizCreate } from "./quiz/QuizCreate";
import { QuizEdit } from "./quiz/QuizEdit";
import { QuizShow } from "./quiz/QuizShow";
import { UserList } from "./user/UserList";
import { UserCreate } from "./user/UserCreate";
import { UserEdit } from "./user/UserEdit";
import { UserShow } from "./user/UserShow";
import { jwtAuthProvider } from "./auth-provider/ra-auth-jwt";

const App = (): React.ReactElement => {
  const [dataProvider, setDataProvider] = useState<DataProvider | null>(null);
  useEffect(() => {
    buildGraphQLProvider
      .then((provider: any) => {
        setDataProvider(() => provider);
      })
      .catch((error: any) => {
        console.log(error);
      });
  }, []);
  if (!dataProvider) {
    return <div>Loading</div>;
  }
  return (
    <div className="App">
      <Admin
        title={"LearningEnglishWordsBot"}
        dataProvider={dataProvider}
        authProvider={jwtAuthProvider}
        theme={theme}
        dashboard={Dashboard}
        loginPage={Login}
      >
        <Resource
          name="Word"
          list={WordList}
          edit={WordEdit}
          create={WordCreate}
          show={WordShow}
        />
        <Resource
          name="UserWord"
          list={UserWordList}
          edit={UserWordEdit}
          create={UserWordCreate}
          show={UserWordShow}
        />
        <Resource
          name="Quiz"
          list={QuizList}
          edit={QuizEdit}
          create={QuizCreate}
          show={QuizShow}
        />
        <Resource
          name="User"
          list={UserList}
          edit={UserEdit}
          create={UserCreate}
          show={UserShow}
        />
      </Admin>
    </div>
  );
};

export default App;
